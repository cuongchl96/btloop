    private void jlistSubjectValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jlistSubjectValueChanged
        // TODO add your handling code here:
        this.btSave.setEnabled(true);
        this.tfCodeSubject.setEnabled(true);
        this.tfNameSubject.setEnabled(true);
        this.spNumberChapter.setEnabled(true);
        this.taOverview.setEnabled(true);
        this.btDeleteSubject.setEnabled(true);
        this.lbThongBao.setText("Thay đổi thông tin môn học. Các trường đánh dấu (*) không được để trống.");
        this.lbThongBao.setVisible(true);

        String selectedMonHoc = this.jlistSubject.getSelectedValue();
        // Bắt sự kiện khi select, bỏ qua sự kiện deselect trên danh sách môn học
        if (evt.getValueIsAdjusting()) {
            for (int i = 0; i < this.listSubject.size(); i++) {
                Subject temp = listSubject.get(i);
                if (selectedMonHoc.equalsIgnoreCase(temp.getCodeSubject())) {
                    this.selectedIndex = i;
                    this.tfNameSubject.setText(temp.getNameSubject());
                    this.tfCodeSubject.setText(temp.getCodeSubject());
                    this.spNumberChapter.setValue(temp.getNumberChapter());
                    this.taOverview.setText(temp.getOverview());
                    break;
                }
            }
        }
    }//GEN-LAST:event_jlistSubjectValueChanged

