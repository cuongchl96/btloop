    private void btSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSaveActionPerformed

        Subject subject;
        String nameSubject;
        String codeSubject;
        int numberChapter;
        String overview;

        this.lbThongBaoNameSubject.setVisible(false);
        this.lbThongBaoCodeSubject.setVisible(false);
        // Nếu đã ấn vào nút Thêm môn học
        if (this.clickAddSubject) {
            nameSubject = this.tfNameSubject.getText();
            codeSubject = this.tfCodeSubject.getText();
            numberChapter = (int) this.spNumberChapter.getValue();
            overview = this.taOverview.getText();
            if (nameSubject.equals("") || codeSubject.equals("")) {
                if (nameSubject.equals("")) {
                    this.lbThongBaoNameSubject.setVisible(true);
                }
                if (codeSubject.equals("")) {
                    this.lbThongBaoCodeSubject.setVisible(true);
                }
                return;
            } else {
                subject = new Subject(nameSubject, codeSubject, numberChapter, overview);
                this.listSubject.add(subject);
                int i = this.listSubject.size() - 1;
                this.modelListSubject.add(i, listSubject.get(i).getCodeSubject());
                this.jlistSubject.setModel(modelListSubject);
                this.writeListSubject();
            }
        } else {
            Subject current = listSubject.get(this.selectedIndex);
            nameSubject = this.tfNameSubject.getText();
            codeSubject = this.tfCodeSubject.getText();
            numberChapter = (int) this.spNumberChapter.getValue();
            overview = this.taOverview.getText();
            if (nameSubject.equals("") || codeSubject.equals("")) {
                if (nameSubject.equals("")) {
                    this.lbThongBaoNameSubject.setVisible(true);
                }
                if (codeSubject.equals("")) {
                    this.lbThongBaoCodeSubject.setVisible(true);
                }
                return;
            } else {
                current.setNameSubject(this.tfNameSubject.getText());
                current.setCodeSubject(this.tfCodeSubject.getText());
                current.setNumberChapter((int) this.spNumberChapter.getValue());
                current.setOverview(this.taOverview.getText());
                this.writeListSubject();
                
                modelListSubject.remove(this.selectedIndex);
                modelListSubject.add(this.selectedIndex, this.tfCodeSubject.getText());
                this.jlistSubject.setModel(modelListSubject);
            }

        }
    }//GEN-LAST:event_btSaveActionPerformed

